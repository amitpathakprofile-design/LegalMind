def augment_dataset(df, target_samples_per_class=350, techniques=['all']):
    """
    Apply multiple augmentation techniques to balance dataset
    """
    print("\n" + "="*70)
    print("AUGMENTING TRAINING DATA")
    print("="*70)

    augmented_data = []
    class_counts = df['label'].value_counts()

    # Available augmentation techniques
    aug_functions = {
        'synonym': augmenter.augment_synonym_replacement,
        'contextual': augmenter.augment_contextual_substitution,
        'backtrans': augmenter.augment_back_translation,
        'deletion': augmenter.augment_random_deletion,
        'swap': augmenter.augment_random_swap,
        'shuffle': augmenter.augment_sentence_shuffling
    }

    if 'all' in techniques:
        techniques = list(aug_functions.keys())

    for label_id in sorted(df['label'].unique()):
        class_df = df[df['label'] == label_id]
        current_count = len(class_df)

        # Add original data
        augmented_data.append(class_df)

        # Augment if below target
        if current_count < target_samples_per_class:
            needed = target_samples_per_class - current_count
            print(f"\nClass {label_id} ({label_names[label_id]}):")
            print(f"  Current: {current_count} samples")
            print(f"  Target: {target_samples_per_class} samples")
            print(f"  Generating: {needed} augmented samples")

            augmented_samples = []
            samples_generated = 0

            # Cycle through augmentation techniques
            technique_cycle = 0

            while samples_generated < needed:
                # Sample a random text from this class
                sample = class_df.sample(n=1).iloc[0]
                text = sample['text']

                # Select augmentation technique (rotate through them)
                technique_name = techniques[technique_cycle % len(techniques)]
                aug_func = aug_functions[technique_name]

                try:
                    # Apply augmentation
                    aug_text = aug_func(text)

                    # Ensure augmented text is different and valid
                    if aug_text and aug_text != text and len(aug_text) > 20:
                        augmented_samples.append({
                            'text': aug_text,
                            'label': label_id,
                            'augmentation': technique_name
                        })
                        samples_generated += 1

                        if samples_generated % 10 == 0:
                            print(f"    Generated: {samples_generated}/{needed}")

                except Exception as e:
                    print(f"    Augmentation failed: {e}")

                technique_cycle += 1

                # Safety break
                if technique_cycle > needed * 3:
                    break

            if augmented_samples:
                augmented_data.append(pd.DataFrame(augmented_samples))
                print(f"  ✓ Successfully generated {len(augmented_samples)} samples")

    final_df = pd.concat(augmented_data, ignore_index=True)

    print(f"\n{'='*70}")
    print(f"AUGMENTATION COMPLETE")
    print(f"{'='*70}")
    print(f"Original dataset: {len(df)} samples")
    print(f"Augmented dataset: {len(final_df)} samples (+{len(final_df) - len(df)})")

    print("\nFinal Class Distribution:")
    for label_id, count in final_df['label'].value_counts().sort_index().items():
        print(f"  Label {label_id} ({label_names[label_id]}): {count} samples")

    return final_df

