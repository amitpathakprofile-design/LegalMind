class FocalLossWithLabelSmoothing(nn.Module):
    """
    Focal Loss + Label Smoothing for better generalization
    """
    def __init__(self, alpha=None, gamma=2.0, smoothing=0.1, reduction='mean'):
        super().__init__()
        self.alpha = alpha
        self.gamma = gamma
        self.smoothing = smoothing
        self.reduction = reduction

    def forward(self, inputs, targets):
        num_classes = inputs.size(1)

        # Label smoothing
        with torch.no_grad():
            smooth_labels = torch.full_like(inputs, self.smoothing / (num_classes - 1))
            smooth_labels.scatter_(1, targets.unsqueeze(1), 1.0 - self.smoothing)

        # Focal loss
        log_probs = F.log_softmax(inputs, dim=1)
        probs = torch.exp(log_probs)

        ce_loss = -(smooth_labels * log_probs).sum(dim=1)

        # Apply focal term
        pt = (smooth_labels * probs).sum(dim=1)
        focal_loss = ((1 - pt) ** self.gamma) * ce_loss

        # Apply class weights if provided
        if self.alpha is not None:
            alpha_t = self.alpha[targets]
            focal_loss = alpha_t * focal_loss

        if self.reduction == 'mean':
            return focal_loss.mean()
        return focal_loss

class EnhancedFocalLossTrainer(Trainer):
    """Enhanced Trainer with Focal Loss + Label Smoothing"""

    def __init__(self, *args, class_weights=None, use_focal=True, label_smoothing=0.1, **kwargs):
        super().__init__(*args, **kwargs)
        self.class_weights = class_weights

        self.loss_fn = FocalLossWithLabelSmoothing(
            alpha=class_weights,
            gamma=2.0,
            smoothing=label_smoothing
        )

    def compute_loss(self, model, inputs, return_outputs=False, **kwargs):
        labels = inputs.pop("labels")
        outputs = model(**inputs)
        logits = outputs.logits
        loss = self.loss_fn(logits, labels)
        return (loss, outputs) if return_outputs else loss

