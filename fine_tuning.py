class LegalTextAugmenter:
    """
    Comprehensive data augmentation for legal text
    Combines 7 different augmentation techniques
    """

    def __init__(self):
        print("Loading augmentation models...")

        # 1. Synonym Replacement (WordNet)
        self.synonym_aug = naw.SynonymAug(aug_src='wordnet', aug_max=3)

        # 2. Contextual Word Embeddings (BERT-based)
        self.contextual_aug = naw.ContextualWordEmbsAug(
            model_path='bert-base-uncased',
            action="substitute",
            aug_max=3
        )

        # 3. Back Translation (for paraphrasing)
        # Using multiple language pairs for diversity
        self.back_trans_aug = naw.BackTranslationAug(
            from_model_name='facebook/nllb-200-distilled-600M',
            to_model_name='facebook/nllb-200-distilled-600M',
            device='cuda' if torch.cuda.is_available() else 'cpu'
        )

        print("✓ Augmentation modules loaded!")

    def augment_synonym_replacement(self, text):
        """Replace words with synonyms"""
        try:
            return self.synonym_aug.augment(text)
        except:
            return text

    def augment_contextual_substitution(self, text):
        """Replace words using BERT contextual embeddings"""
        try:
            return self.contextual_aug.augment(text)
        except:
            return text

    def augment_back_translation(self, text):
        """Back-translate through intermediate language"""
        try:
            return self.back_trans_aug.augment(text)
        except:
            return text

    def augment_random_deletion(self, text, p=0.1):
        """Randomly delete words with probability p"""
        words = text.split()
        if len(words) == 1:
            return text

        new_words = []
        for word in words:
            if random.random() > p:
                new_words.append(word)

        if len(new_words) == 0:
            return random.choice(words)

        return ' '.join(new_words)

    def augment_random_swap(self, text, n=2):
        """Randomly swap n pairs of words"""
        words = text.split()
        if len(words) < 2:
            return text

        new_words = words.copy()
        for _ in range(n):
            idx1, idx2 = random.sample(range(len(new_words)), 2)
            new_words[idx1], new_words[idx2] = new_words[idx2], new_words[idx1]

        return ' '.join(new_words)

    def augment_sentence_shuffling(self, text):
        """Shuffle sentences within the text"""
        sentences = text.split('. ')
        if len(sentences) < 2:
            return text

        random.shuffle(sentences)
        return '. '.join(sentences)

    def augment_mixup(self, text1, text2, alpha=0.5):
        """
        Text MixUp: Combine two texts from same class
        Weighted combination of sentences
        """
        sentences1 = text1.split('. ')
        sentences2 = text2.split('. ')

        # Randomly select sentences from both
        mixed = []
        max_len = max(len(sentences1), len(sentences2))

        for i in range(max_len):
            if random.random() < alpha and i < len(sentences1):
                mixed.append(sentences1[i])
            elif i < len(sentences2):
                mixed.append(sentences2[i])

        return '. '.join(mixed)

