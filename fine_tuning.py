import torch
import numpy as np
from transformers import AutoTokenizer, AutoModelForSequenceClassification
from typing import Dict, List
import os

class SimpleLegalEnsemble:
    """
    Simple ensemble averaging Legal-BERT-Base and DeBERTa-v3
    Expected performance: 92-94% accuracy
    """

    def __init__(self, legal_bert_path, deberta_path, device='auto'):
        """
        Initialize ensemble with two models

        Args:
            legal_bert_path: Path to Legal-BERT-Base model
            deberta_path: Path to DeBERTa-v3 model
            device: 'cuda', 'cpu', or 'auto'
        """
        # Set device
        if device == 'auto':
            self.device = 'cuda' if torch.cuda.is_available() else 'cpu'
        else:
            self.device = device

        print(f"Loading models on {self.device}...")

        # Load Legal-BERT-Base
        print("  Loading Legal-BERT-Base...")
        self.legal_bert_tokenizer = AutoTokenizer.from_pretrained(legal_bert_path)
        self.legal_bert_model = AutoModelForSequenceClassification.from_pretrained(legal_bert_path)
        self.legal_bert_model.to(self.device)
        self.legal_bert_model.eval()

        # Load DeBERTa-v3
        print("  Loading DeBERTa-v3...")
        self.deberta_tokenizer = AutoTokenizer.from_pretrained(deberta_path)
        self.deberta_model = AutoModelForSequenceClassification.from_pretrained(deberta_path)
        self.deberta_model.to(self.device)
        self.deberta_model.eval()

        # Label mapping
        self.id2label = {
            0: "Safe/Standard",
            1: "Unilateral Termination",
            2: "Unlimited Liability",
            3: "Non-Compete"
        }

        print("✓ Both models loaded successfully!")

    def predict(self, text: str, return_all_scores: bool = True) -> Dict:
        """
        Predict class by averaging predictions from both models

        Args:
            text: Contract clause text
            return_all_scores: Whether to return scores for all classes

        Returns:
            Dictionary with prediction results
        """
        with torch.no_grad():
            # Get Legal-BERT prediction
            legal_bert_inputs = self.legal_bert_tokenizer(
                text,
                padding='max_length',
                truncation=True,
                max_length=512,
                return_tensors='pt'
            ).to(self.device)

            legal_bert_outputs = self.legal_bert_model(**legal_bert_inputs)
            legal_bert_probs = torch.softmax(legal_bert_outputs.logits, dim=1).cpu().numpy()[0]

            # Get DeBERTa prediction
            deberta_inputs = self.deberta_tokenizer(
                text,
                padding='max_length',
                truncation=True,
                max_length=512,
                return_tensors='pt'
            ).to(self.device)

            deberta_outputs = self.deberta_model(**deberta_inputs)
            deberta_probs = torch.softmax(deberta_outputs.logits, dim=1).cpu().numpy()[0]

            # Average probabilities (simple averaging)
            avg_probs = (legal_bert_probs + deberta_probs) / 2

            # Get final prediction
            predicted_class = int(np.argmax(avg_probs))
            confidence = float(avg_probs[predicted_class])

            result = {
                'label': self.id2label[predicted_class],
                'label_id': predicted_class,
                'score': confidence
            }

            if return_all_scores:
                result['all_scores'] = {
                    self.id2label[i]: float(avg_probs[i])
                    for i in range(len(avg_probs))
                }
                result['individual_predictions'] = {
                    'legal_bert': {
                        'label': self.id2label[int(np.argmax(legal_bert_probs))],
                        'score': float(legal_bert_probs[np.argmax(legal_bert_probs)])
                    },
                    'deberta': {
                        'label': self.id2label[int(np.argmax(deberta_probs))],
                        'score': float(deberta_probs[np.argmax(deberta_probs)])
                    }
                }

            return result

    def predict_batch(self, texts: List[str], batch_size: int = 8) -> List[Dict]:
        """
        Predict multiple texts in batches

        Args:
            texts: List of contract clause texts
            batch_size: Batch size for processing

        Returns:
            List of prediction dictionaries
        """
        results = []

        for i in range(0, len(texts), batch_size):
            batch_texts = texts[i:i + batch_size]

            with torch.no_grad():
                # Legal-BERT batch prediction
                legal_bert_inputs = self.legal_bert_tokenizer(
                    batch_texts,
                    padding='max_length',
                    truncation=True,
                    max_length=512,
                    return_tensors='pt'
                ).to(self.device)

                legal_bert_outputs = self.legal_bert_model(**legal_bert_inputs)
                legal_bert_probs = torch.softmax(legal_bert_outputs.logits, dim=1).cpu().numpy()

                # DeBERTa batch prediction
                deberta_inputs = self.deberta_tokenizer(
                    batch_texts,
                    padding='max_length',
                    truncation=True,
                    max_length=512,
                    return_tensors='pt'
                ).to(self.device)

                deberta_outputs = self.deberta_model(**deberta_inputs)
                deberta_probs = torch.softmax(deberta_outputs.logits, dim=1).cpu().numpy()

                # Average predictions
                avg_probs = (legal_bert_probs + deberta_probs) / 2

                # Process each result
                for probs in avg_probs:
                    predicted_class = int(np.argmax(probs))
                    results.append({
                        'label': self.id2label[predicted_class],
                        'label_id': predicted_class,
                        'score': float(probs[predicted_class]),
                        'all_scores': {
                            self.id2label[i]: float(probs[i])
                            for i in range(len(probs))
                        }
                    })

        return results


